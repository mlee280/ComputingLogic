# Variables to hold the animal names
c = "cats"  # Variable c holds the word "cats."
d = "dogs"  # Variable d holds the word "dogs."

# Construct the sentence to print
s = f"It is raining {c} and {d}."  # Using f-string for formatting the sentence

# Print the constructed sentence
print(s)  # This will output: It is raining cats and dogs.
